var Marks = prompt("Enter  your Marks");

if (Marks >= 80)

    console.log("A+");

else if (Marks >= 70)

    console.log("A");
else if (Marks >= 60)

    console.log("A-");
else if (Marks >= 50)

    console.log("B");
else if (Marks >= 40)

    console.log("C");
else if (Marks >= 33)

    console.log("D");

else
    console.log("Fail");